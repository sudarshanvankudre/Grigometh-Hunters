package byog.Core.RoomsAndHallways;

public class Room {
    protected int x;
    protected int y;
    protected int height;
    protected int width;

    public Room(int x, int y, int height, int width) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
